package com.arora.player.model;


import javax.persistence.*;
import java.util.Set;

@Entity
@Table(name = "player")
public class Player {
    
	private Long userid;

	private String games_ids;
	private String teams_ids;
	private String tournaments_ids;

    
    @Id
    public Long getUserid() {
        return userid;
    }

    public void setUserid(Long userid) {
        this.userid = userid;
    }

	public String getGames_ids() {
		return games_ids;
	}

	public void setGames_ids(String games_ids) {
		this.games_ids = games_ids;
	}

	public String getTeams_ids() {
		return teams_ids;
	}

	public void setTeams_ids(String teams_ids) {
		this.teams_ids = teams_ids;
	}

	public String getTournaments_ids() {
		return tournaments_ids;
	}

	public void setTournaments_ids(String tournaments_ids) {
		this.tournaments_ids = tournaments_ids;
	}

   
    
    
}
